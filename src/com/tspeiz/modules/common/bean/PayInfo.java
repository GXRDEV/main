package com.tspeiz.modules.common.bean;


public class PayInfo {
	private Object transactionId;
	private Object outTradeNo;
	private Object money;
	private Object refundStatus;
	private Object refundTime;
	private Object refundResult;
	private Object payStatus;
	public Object getTransactionId() {
		return transactionId;
	}
	public void setTransactionId(Object transactionId) {
		this.transactionId = transactionId;
	}
	public Object getOutTradeNo() {
		return outTradeNo;
	}
	public void setOutTradeNo(Object outTradeNo) {
		this.outTradeNo = outTradeNo;
	}
	public Object getMoney() {
		return money;
	}
	public void setMoney(Object money) {
		this.money = money;
	}
	public Object getRefundStatus() {
		return refundStatus;
	}
	public void setRefundStatus(Object refundStatus) {
		this.refundStatus = refundStatus;
	}
	public Object getRefundTime() {
		return refundTime;
	}
	public void setRefundTime(Object refundTime) {
		this.refundTime = refundTime;
	}
	public Object getRefundResult() {
		return refundResult;
	}
	public void setRefundResult(Object refundResult) {
		this.refundResult = refundResult;
	}
	public Object getPayStatus() {
		return payStatus;
	}
	public void setPayStatus(Object payStatus) {
		this.payStatus = payStatus;
	}
	
	
}
